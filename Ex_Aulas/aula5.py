# tipo de dado bool(boolean)
# Ao questionar algo em um programa, 
# só existem duas respostas possíveis: 
# sim (True) e não (False).
# Existem vários tipos de operadores para "questionar".
# Dentre eles o ==, que é o operador lógico que
# questiona se um valor é igual a outro
print('10 é igual a 10?', 10 == 10) # resposta = true - verdadeiro
print ('10 é igual a 11?', 10 == 11) # resposta = false - falso
print(type(10 == 10))
print(type(10 == 11))
